import math

# math.sqrt():  returns the square root of a number
print("sqrt(64)\t\t", math.sqrt(64))                # sqrt(64)                 8.0

# math.ceil(): rounds a number upwards to its nearest integer
print("ceil(1.4)\t\t", math.ceil(1.4))              # ceil(1.4)                2

# math.floor(): rounds a number downwards to its nearest integer
print("floor(1.4)\t\t", math.floor(1.4))            # floor(1.4)               1

# math.pi: constant, returns the value of PI (3.14)
print("pi\t\t", math.pi)                            # pi               3.141592653589793
